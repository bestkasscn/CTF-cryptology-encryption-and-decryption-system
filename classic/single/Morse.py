morse_table = {
    'A': '.-', 'N': '-.', '.': '.-.-.-', '+': '.-.-.', '1': '.----',
    'B': '-...', 'O': '---', ',': '--..--', '_': '..--.-', '2': '..---',
    'C': '-.-.', 'P': '.--.', ':': '---...', '$': '...-..-', '3': '...--',
    'D': '-..', 'Q': '--.-', '"': '.-..-.', '&': '.-...', '4': '....-',
    'E': '.', 'R': '.-.', '\'': '.----.', '/': '-..-.', '5': '.....',
    'F': '..-.', 'S': '...', '!': '-.-.--', '6': '-....',
    'G': '--.', 'T': '-', '?': '..--..', '7': '--...',
    'H': '....', 'U': '..-', '@': '.--.-.', '8': '---..',
    'I': '..', 'V': '...-', '-': '-....-', '9': '----.',
    'J': '.---', 'W': '.--', ';': '-.-.-.', '0': '-----',
    'K': '-.-', 'X': '-..-', '(': '-.--.',
    'L': '.-..', 'Y': '-.--', ')': '-.--.-',
    'M': '--', 'Z': '--..', '=': '-...-',
}
morse_dir = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789.!@#$%^&*()_+:"?/=,\''


def morse_encrypt(plaintext: str) -> str:
    res = ''
    for s in plaintext:
        if s.upper() not in morse_table:
            return "无法解密"
        res += ''.join(morse_table.get(s.upper())) + " "
    return res


def morse_decrypt(plaintext: str) -> str:
    table = plaintext.split(" ")
    res = ''
    for s in table:
        for i in morse_dir:
            if morse_table.get(i) == s:
                res += ''.join(i)
    return res


print(morse_decrypt('- .... .. ... ..--.- .. ... ..--.- .- ..--.- - . ... -'))
